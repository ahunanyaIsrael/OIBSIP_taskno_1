    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int pnr = Integer.parseInt(pnr_txt.getText().trim());
        
        try {
            pst= conn.prepareStatement("DELETE FROM traindata WHERE PNR = ?");
            pst.setInt(1, pnr);
            int rowsDeleted = pst.executeUpdate();
            
            if(rowsDeleted > 0){
                JOptionPane.showMessageDialog(this," Record has been deleted");
            }
            else{
                 JOptionPane.showMessageDialog(this," Record not found with this PNR" + pnr );
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cancellation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


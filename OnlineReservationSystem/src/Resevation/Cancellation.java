    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            // TODO add your handling code here:
    
        
        try {
            
            int pnr = Integer.parseInt(pnr_txt.getText().trim());

            pst = conn.prepareStatement("Select * from traindata where PNR = ?");
            pst.setInt(1,pnr);  
                        
            rs = pst.executeQuery();
            
            if(rs.next()){
                
                pnr_txt.setText(rs.getString("PNR"));
                tnumber_txt.setText(rs.getString("Train_ID"));
                tname_txt.setText(rs.getString("Train_Name")); 
                class_txt.setText(rs.getString("Class"));
                from_txt.setText(rs.getString("From_place"));
                to_txt.setText(rs.getString("To_place"));
                s_date.setDate(rs.getDate("DOD"));
                JOptionPane.showMessageDialog(this,"Record found");
                
            }else{
                JOptionPane.showMessageDialog(this,"Record not found");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cancellation.class.getName()).log(Level.SEVERE, null, ex);
        }catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(this, "Please enter valid numeric values for PNR and Train Number.", "Input Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_jButton1ActionPerformed


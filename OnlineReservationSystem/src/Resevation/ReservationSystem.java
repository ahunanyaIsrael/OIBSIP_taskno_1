    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        var t_num = Integer.parseInt(tnum_txt.getText());
        var t_name = tname_txt.getText();
        var t_class = class_txt.getText();
        var t_to = to_txt.getText();
        var t_from = from_txt.getText();
        java.util.Date S_date = dod.getDate();
     
        int pnr;
        Random num = new Random();
        pnr = 1000000 + num.nextInt(9000000);
        
        
        try {
            pst = conn.prepareStatement("insert into traindata(PNR,Train_ID,Train_Name,Class,From_place,To_place, DOD)values(?,?,?,?,?,?,?)");
            pst.setInt(1,pnr);  
            pst.setInt(2,t_num);
            pst.setString(3,t_name);
            pst.setString(4, t_class);
            pst.setString(5, t_from);
            pst.setString(6, t_to);
            pst.setDate(7,new java.sql.Date(S_date.getTime()));
            
            
            int result = pst.executeUpdate();
            
            if(result == 1){
                JOptionPane.showMessageDialog(this, "Data inserted Successfully");
                TrainDetails();
            }else{
                JOptionPane.showMessageDialog(this, "Unsuccessful");
            }
           
                    } catch (SQLException ex) {
            Logger.getLogger(ReservationSystem.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

